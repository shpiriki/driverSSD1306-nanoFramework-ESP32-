            get
            {
                int index = 0;
                int code = (int)character;

                // 1. Стандартная латиница и знаки (ASCII 32 - 127)
                if (code >= 32 && code <= 127)
                {
                    index = code - 32;
                }
                // 2. Русские буквы А - я (в кодировке CP1251 это 192 - 255)
                else if (code >= 192 && code <= 255)
                {
                    index = (code - 192) + 96; // Сдвиг после латиницы
                }
                // 3. Отдельно обрабатываем буквы Ё и ё
                else if (code == 168) index = 160; // Ё
                else if (code == 184) index = 161; // ё
                else
                {
                    index = 0; // Если символ не найден — рисуем пробел
                }

                byte[] result = new byte[Width];
                Array.Copy(FontData, index * Width, result, 0, Width);
                return result;
            }
        }

